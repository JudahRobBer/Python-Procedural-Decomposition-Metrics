

############### Features ################
# Spire and stem in the middle
def spire_stem() -> None:
    print('      ||      ')
    print('      ||      ')
    
# Topshell/base
def topshell_base() -> None:
    print('   __{{}}__   ')
    print('__{::::::::}__')
    print('||||||||||||||')
    
# Bottomshell
def botshell() -> None:
    print('   |%%%%%%|   ')
    print('   |%%%%%%|   ')
    print('   |%%%%%%|   ')
    print('   |%%%%%%|   ')

# Middle
def mid() -> None:
    print('(_)()()()()(_)')
    print('  (_)()()(_)  ')
    print('  ____][____  ')
    print('   ___][___   ')
    
########## Define Main Function ###########
def main() -> None:
    spire_stem()
    topshell_base()
    mid()
    spire_stem()
    botshell()
    topshell_base()

########## Calling Main Function ##########
main()
    
    